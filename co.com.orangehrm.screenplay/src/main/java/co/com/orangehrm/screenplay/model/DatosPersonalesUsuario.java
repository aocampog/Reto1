package co.com.orangehrm.screenplay.model;

public class DatosPersonalesUsuario {
		
	private String primerNombre;
	private String segundoNombre;
	private String apellido;
	private String fechaNacimiento;
	private String licenciaConduccion;
	private String fechaVencimientoConduccion;
	private String usuario;
	private String servicioMilitar;
	private String hobbies;
	
	public String getPrimerNombre() {
		return primerNombre;
	}
	public void setPrimerNombre(String primerNombre) {
		this.primerNombre = primerNombre;
	}
	public String getSegundoNombre() {
		return segundoNombre;
	}
	public void setSegundoNombre(String segundoNombre) {
		this.segundoNombre = segundoNombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public String getFechaNacimiento() {
		return fechaNacimiento;
	}
	public void setFechaNacimiento(String fechaNacimiento) {
		this.fechaNacimiento = fechaNacimiento;
	}
	public String getLicenciaConduccion() {
		return licenciaConduccion;
	}
	public void setLicenciaConduccion(String licenciaConduccion) {
		this.licenciaConduccion = licenciaConduccion;
	}
	public String getFechaVencimientoConduccion() {
		return fechaVencimientoConduccion;
	}
	public void setFechaVencimientoConduccion(String fechaVencimientoConduccion) {
		this.fechaVencimientoConduccion = fechaVencimientoConduccion;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getServicioMilitar() {
		return servicioMilitar;
	}
	public void setServicioMilitar(String servicioMilitar) {
		this.servicioMilitar = servicioMilitar;
	}
	public String getHobbies() {
		return hobbies;
	}
	public void setHobbies(String hobbies) {
		this.hobbies = hobbies;
	}
	
	

}
